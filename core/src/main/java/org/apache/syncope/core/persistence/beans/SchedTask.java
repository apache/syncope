package org.apache.syncope.core.persistence.beans;

import javax.persistence.Entity;
import javax.validation.constraints.NotNull;
import org.apache.syncope.core.persistence.validation.entity.SchedTaskCheck;

@Entity
@SchedTaskCheck
public class SchedTask extends Task {

    private static final long serialVersionUID = -4141057723006682562L;

    protected String cronExpression;

    @NotNull
    protected String jobClassName;

    protected String name;

    protected String description;

    public String getCronExpression() {
        return cronExpression;
    }

    public void setCronExpression(String cronExpression) {
        this.cronExpression = cronExpression;
    }

    public String getJobClassName() {
        return jobClassName;
    }

    public void setJobClassName(String jobClassName) {
        this.jobClassName = jobClassName;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
