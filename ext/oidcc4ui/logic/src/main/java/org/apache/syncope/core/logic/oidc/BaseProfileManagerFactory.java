package org.apache.syncope.core.logic.oidc;

import org.pac4j.core.context.WebContext;
import org.pac4j.core.context.session.SessionStore;
import org.pac4j.core.profile.ProfileManager;
import org.pac4j.core.profile.factory.ProfileManagerFactory;

public final class BaseProfileManagerFactory implements ProfileManagerFactory {

    public static final BaseProfileManagerFactory INSTANCE = new BaseProfileManagerFactory();

    private BaseProfileManagerFactory() {
        // private constructor for singleton
    }

    @Override
    public ProfileManager apply(final WebContext wc, final SessionStore ss) {
        return new ProfileManager(wc, ss);
    }
}
